/*
    VARIABLES == containers/boxed
        let - mutable/changeable values
        const - unmutable/unchangeable values
        var - same as let, but has few scoping differences;
*/

let myName = "Romeena";
const myAge = 36;

// display in console
console.log(myName);
console.log(myAge);

// editing let's value
myName = "Anne";
console.log(myName);

// myAge = 30
